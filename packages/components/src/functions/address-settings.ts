export function sortAddressFields(fields, localeSettings) {
  return fields.sort((a, b) => {
    const priorityA = localeSettings[a.name]?.priority || 100;
    const priorityB = localeSettings[b.name]?.priority || 100;
    return priorityA - priorityB;
  });
}
